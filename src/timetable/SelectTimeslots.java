    public final void fillCList() {
        for (int i = 0; i < secList.size(); i++) {

            boolean flag = true;

            for (int j = 0; j < courseList.size(); j++) {
                if (courseList.get(j).getCode().equals(secList.get(i).getCourse().getCode())) {
                    flag = false;
                    break;
                }
            }

            if (flag) {
                courseList.add(secList.get(i).getCourse());
            }
        }
    }

