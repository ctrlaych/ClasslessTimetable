    private void sListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sListActionPerformed
        // TODO add your handling code here:
        timing.setText(" ");
        day.setText(" ");
        venue.setText(" ");

        if (sList.getSelectedItem() != null) {
            if (!sList.getSelectedItem().toString().equals("Select Section")) {

                for (int i = 0; i < secList.size(); i++) {
                    if (secList.get(i).getName().equals(sList.getSelectedItem().toString())) {

                        LocalTime lt = secList.get(i).getTime();
                        TIME_ = lt;
                        String secDay = "";

                        if (!cList.getSelectedItem().toString().contains(" Lab")) {
                            lt = lt.plusHours(1);
                            lt = lt.plusMinutes(30);

                            if (secList.get(i).getDay().equals("Monday")) {
                                secDay = ", Wednesday";
                            } else if (secList.get(i).getDay().equals("Tuesday")) {
                                secDay = ", Thursday";
                            } else if (secList.get(i).getDay().equals("Wednesday")) {
                                secDay = ", Friday";
                            }

                        } else {
                            lt = lt.plusHours(3);
                        }

                        timing.setText(secList.get(i).getTime().toString() + " - " + lt.toString());
                        time_ = secList.get(i).getTime().toString();
                        day.setText(secList.get(i).getDay() + secDay);
                        day_ = secList.get(i).getDay();
                        venue.setText(secList.get(i).getRoom());
                        venue_ = secList.get(i).getRoom();
                        C_ = secList.get(i).getCourse();
                        roomlmt_ = secList.get(i).getRoomLimit();
                        break;
                    }
                }
            }
        }

        //

    }//GEN-LAST:event_sListActionPerformed

