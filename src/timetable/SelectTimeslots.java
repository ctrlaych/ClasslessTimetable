    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        if (cList.getSelectedItem().toString().equals("Select Course") || sList.getSelectedItem().toString().equals("Select Section")) {
            JOptionPane.showMessageDialog(null, "Please enter all fields");
        } else {
            Connection dbconn = Database.con();
            int userId = 0;
            int tId = 0;

            if (dbconn != null) {
                try {

                    PreparedStatement st;

                    st = dbconn.prepareStatement("UPDATE section SET instructorId = ? WHERE time = ? and day = ? and room = ?");

                    st.setInt(1, T.getId());
                    time_ += ":00";
                    st.setString(2, time_);
                    st.setString(3, day_);
                    st.setString(4, venue_);
                    st.executeUpdate();
                   
                    Section s = new Section(sList.getSelectedItem().toString(), day_, TIME_, venue_, roomlmt_, C_, T);
                    T.getSectionList().add(s);

                    JOptionPane.showMessageDialog(null, "Timeslot added successfully");

                    this.dispose();
                    new SelectTimeslots(T, secList).setVisible(true);

                } catch (SQLException ex) {
                    System.out.println(ex);
                }
            } else {
                System.out.println("Connection not availabale");
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

